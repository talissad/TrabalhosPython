def soma(n1,n2):
    return n1 + n2

def subtracao(n1,n2):
    return n1 - n2
    
def multiplicacao(n1,n2):
    return n1 * n2

def divisao_fracionada(n1,n2):
    return n1 / n2

def divisao_inteira(n1,n2):
    return n1 // n2

def resto(n1,n2):
    return n1 % n2

def raiz(n1,n2):
    return n1 * (1/n2)